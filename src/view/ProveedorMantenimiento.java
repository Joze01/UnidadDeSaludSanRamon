    private void leerBean() {
        txtID.setText(String.valueOf(bean.getIdProveedor()));
        txtNombre.setText(bean.getNombreProveedor());
        txtDescripcion.setText(bean.getDescripcionProveedor());
    }

    private void llenarBean() {
        bean.setNombreProveedor(txtNombre.getText());
        bean.setDescripcionProveedor(txtDescripcion.getText());
    }

