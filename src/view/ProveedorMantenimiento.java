    private void tblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProveedoresMouseClicked
        DefaultTableModel modeloTabla = (DefaultTableModel) tblProveedores.getModel();
        int filaSeleccionada = tblProveedores.getSelectedRow();

        bean.setIdProveedor(Integer.valueOf(modeloTabla.getValueAt(filaSeleccionada, 0).toString()));
        bean.setNombreProveedor(modeloTabla.getValueAt(filaSeleccionada, 1).toString());
        bean.setDescripcionProveedor(modeloTabla.getValueAt(filaSeleccionada, 2).toString());

        leerBean();

        btnIngresar.setEnabled(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_tblProveedoresMouseClicked

