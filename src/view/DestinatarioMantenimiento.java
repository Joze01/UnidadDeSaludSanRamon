    private void tblDestinatariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDestinatariosMouseClicked
        DefaultTableModel modeloTabla = (DefaultTableModel) tblDestinatarios.getModel();
        int filaSeleccionada = tblDestinatarios.getSelectedRow();

        bean.setIdDestinatario(Integer.valueOf(modeloTabla.getValueAt(filaSeleccionada, 0).toString()));
        bean.setNombreDestinatario(modeloTabla.getValueAt(filaSeleccionada, 1).toString());
        bean.setDescripcionDestintario(modeloTabla.getValueAt(filaSeleccionada, 2).toString());

        leerBean();

        btnIngresar.setEnabled(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }//GEN-LAST:event_tblDestinatariosMouseClicked

